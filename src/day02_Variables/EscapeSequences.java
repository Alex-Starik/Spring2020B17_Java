package day02_Variables;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello World");
        System.out.println("\tHello World");

        System.out.println("One Two \nThree");
        System.out.println("\n\n\n Hello Cybertek");

        // My favourite TV show is "Games of Thrones"
        System.out.println("My Favourite TV show is \"Game of Thrones\"");
    }
}
